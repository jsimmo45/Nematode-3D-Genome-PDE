#!/usr/bin/env python3
"""
clustering_heatmap.py
=====================
Analyze whole-chromosome Hi-C interaction frequencies using raw contact counts
from HiC-Pro allValidPairs files.  Builds a chromosome-by-chromosome interaction
matrix, applies optional normalization, performs hierarchical clustering, and
generates clustered heatmaps.

Supports pre-PDE samples (intact germline genome, fewer chromosomes) and
post-PDE samples (fragmented somatic genome, more chromosomes) with separate
chromosome order files and configurable label font sizes for each.

Two normalization modes:
  --mccord   : Expected-trans normalization (McCord et al.).  For each pair
               (i,j), expected = 0.5 * (Ci*Cj/(T-Ci) + Ci*Cj/(T-Cj)) where
               Ci/Cj are row marginals and T is the grand total.
               Normalized value = observed / expected.
  --normalize: Row-max normalization (divide each row by its maximum).
  (default)  : No normalization (raw symmetric counts).

Outputs (per sample, in --output_dir):
  - Clustered heatmap (PNG + PDF)
  - Cluster assignments (TSV)

Dependencies:
  numpy, pandas, seaborn, matplotlib, scipy

Example usage:
  python clustering_heatmap.py \\
      --pre_pde_samples \\
          allValidPairs/AG.v50.1cell.allValidPairs \\
          allValidPairs/AG.v50.48hr.allValidPairs \\
          allValidPairs/AG.v50.60hr.allValidPairs \\
      --post_pde_samples \\
          allValidPairs/AG.v50.5day.allValidPairs \\
          allValidPairs/AG.v50.10day.allValidPairs \\
      --order_pre data/chrom_order_pre.txt \\
      --order_post data/chrom_order_post.txt \\
      --mccord \\
      --thresh 2.0 \\
      --label_fontsize_pre 22 \\
      --label_fontsize_post 16 \\
      --hide_row_dendrogram \\
      --dpi 300 \\
      --output_dir results/ \\
      --output_clusters clusters.bed \\
      --output_heatmap heatmap.png \\
      --output_table sorted.xlsx \\
      --output_matrix matrix.xlsx \\
      --output_combined combined.xlsx \\
      --output_pca pca.png \\
      --output_cluster_heatmap cluster_heatmap.png

Input files:
  --pre_pde_samples / --post_pde_samples: HiC-Pro allValidPairs files.
      Column 2 = query chromosome, column 5 = target chromosome.
      These are large files generated by HiC-Pro and are NOT included in the
      repository.  Raw reads are available from SRA under accession SRPXXXXXX;
      regenerate allValidPairs by running HiC-Pro.
  --order_pre / --order_post: Text files listing chromosome names in desired
      display order (one per line).  Included in the repository under data/.
"""

