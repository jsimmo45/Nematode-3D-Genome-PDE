def compute_sample(pairs_file, group_tag, order_list, target_chroms, outdir, args):
    """Pass 1: Build and normalize matrix, run clustering, write cluster file.
    Returns (sample_name, final_matrix, cluster_dict)."""
    base_name = os.path.basename(pairs_file).replace(".allValidPairs","")
    samp_name = f"{base_name}_{group_tag}"
    counts = parse_pairs_files([pairs_file], target_chroms=target_chroms)
    # filter out chrM,chrUn
    counts = {
        c: {o:ct for o,ct in inter.items() if not (o.startswith("chrM") or o.startswith("chrUn"))}
        for c,inter in counts.items() if not (c.startswith("chrM") or c.startswith("chrUn"))
    }
    mat_raw = build_interaction_matrix(counts, order_list)
    mat_sym = symmetrize_matrix(mat_raw)
    
    # Apply normalization based on flags
    if args.mccord:
        mat_fin = normalize_interaction_matrix_expected(mat_sym)
    elif args.normalize:
        mat_fin = normalize_symmetric_by_row_max(mat_sym)
    else:
        mat_fin = mat_sym
        
    cdict,_ = cluster_chromosomes(mat_fin, nclusters=args.nclusters, thresh=args.thresh)
    cdict = relabel_by_frequency(cdict)
    cF = os.path.join(outdir, f"{samp_name}_{args.output_clusters}")
    write_clusters(cdict, cF)
    return samp_name, mat_fin, cdict

def get_global_range(matrices):
    """Compute the global min and max across a list of DataFrames (ignoring NaN/Inf)."""
    all_min, all_max = np.inf, -np.inf
    for mat in matrices:
        finite_vals = mat.values[np.isfinite(mat.values)]
        if finite_vals.size > 0:
            all_min = min(all_min, np.min(finite_vals))
            all_max = max(all_max, np.max(finite_vals))
    if all_min == np.inf:
        all_min, all_max = 0, 1
    return all_min, all_max

def main():
    parser = argparse.ArgumentParser(
        description="Analyze pre-PDE and post-PDE Hi-C data. "
                    "Generates heatmaps and Excel outputs for each sample."
    )
    parser.add_argument("--pre_pde_samples", nargs="+", required=True, help="List of pre-PDE allValidPairs files.")
    parser.add_argument("--post_pde_samples", nargs="*", default=[], help="List of post-PDE allValidPairs files.")
    parser.add_argument("--order_pre", required=True, help="Pre-PDE order file (24 lines).")
    parser.add_argument("--order_post", required=True, help="Post-PDE order file (36 lines).")
    parser.add_argument("--thresh", type=float, default=None, help="Clustering distance threshold.")
    parser.add_argument("--output_table", required=True)
    parser.add_argument("--output_matrix", required=True)
    parser.add_argument("--output_combined", required=True)
    parser.add_argument("--output_clusters", required=True)
    parser.add_argument("--output_heatmap", required=True)
    parser.add_argument("--output_pca", required=True)
    parser.add_argument("--output_cluster_heatmap", required=True)
    parser.add_argument("--nclusters", type=int, default=None)
    parser.add_argument("--target", type=str, default=None)
    parser.add_argument("--normalize", action="store_true", help="Use row-max normalization")
    parser.add_argument("--mccord", action="store_true", help="Use expected-trans (McCord) normalization (overrides --normalize)")
    parser.add_argument("--dpi", type=int, default=600)
    parser.add_argument("--output_dir", default="combined_output")
    parser.add_argument("--label_fontsize_pre", type=int, default=16,
                        help="Font size for chromosome labels on pre-PDE heatmaps (default: 16)")
    parser.add_argument("--label_fontsize_post", type=int, default=12,
                        help="Font size for chromosome labels on post-PDE heatmaps (default: 12)")
    parser.add_argument("--hide_row_dendrogram", action="store_true",
                        help="Hide the left-side (row) dendrogram to save horizontal space")

    args = parser.parse_args()
    if not os.path.exists(args.output_dir):
        os.makedirs(args.output_dir)

    target_chroms = None
    if args.target:
        target_chroms = [ x.strip() for x in args.target.split(",") if x.strip() ]

    order_pre = read_order_file(args.order_pre)
    order_post = read_order_file(args.order_post)

    # ---- Pass 1: Compute all matrices ----
    pre_results = []   # list of (samp_name, mat_fin, cdict)
    post_results = []
    all_matrices = []  # for computing global color range

    print("=== Pass 1: Computing matrices and clusters ===")
    for pf in args.pre_pde_samples:
        samp_name, mat_fin, cdict = compute_sample(pf, "pre", order_pre, target_chroms, args.output_dir, args)
        pre_results.append((samp_name, mat_fin, cdict))
        all_matrices.append(mat_fin)

    for pf in args.post_pde_samples:
        samp_name, mat_fin, cdict = compute_sample(pf, "post", order_post, target_chroms, args.output_dir, args)
        post_results.append((samp_name, mat_fin, cdict))
        all_matrices.append(mat_fin)

    # Compute shared color scale across ALL samples
    global_vmin, global_vmax = get_global_range(all_matrices)
    print(f"\n=== Global color scale: vmin={global_vmin:.4f}, vmax={global_vmax:.4f} ===\n")

    # ---- Pass 2: Plot heatmaps with shared scale ----
    print("=== Pass 2: Plotting heatmaps with shared scale ===")
    for samp_name, mat_fin, cdict in pre_results:
        hF = os.path.join(args.output_dir, f"{samp_name}_{args.output_heatmap}")
        plot_heatmap(mat_fin, samp_name, hF, dpi=args.dpi,
                     global_vmin=global_vmin, global_vmax=global_vmax,
                     label_fontsize=args.label_fontsize_pre,
                     hide_row_dendrogram=args.hide_row_dendrogram)

    for samp_name, mat_fin, cdict in post_results:
        hF = os.path.join(args.output_dir, f"{samp_name}_{args.output_heatmap}")
        plot_heatmap(mat_fin, samp_name, hF, dpi=args.dpi,
                     global_vmin=global_vmin, global_vmax=global_vmax,
                     label_fontsize=args.label_fontsize_post,
                     hide_row_dendrogram=args.hide_row_dendrogram)

    print("\nAll samples processed. Heatmaps and cluster files saved to", args.output_dir)

if __name__=="__main__":
    main()
