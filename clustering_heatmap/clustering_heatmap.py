# -----------------------------
# Helper: Get base chromosome name (strip trailing lowercase letters)
# e.g. "chr01a" => "chr01"
# -----------------------------
def get_base(chrom):
    i = len(chrom)
    while i > 0 and chrom[i - 1].islower():
        i -= 1
    return chrom[:i]

# -----------------------------
# Helper: Convert value to float or -∞
# -----------------------------
def safe_float(val):
    try:
        return float(val)
    except Exception:
        return -np.inf

# -----------------------------
# Helper: Relabel clusters by frequency
# Largest cluster => label=1, next => label=2, etc.
# -----------------------------
def relabel_by_frequency(cluster_dict):
    freq = {}
    for chrom, label in cluster_dict.items():
        freq[label] = freq.get(label, 0) + 1
    sorted_labels = sorted(freq.items(), key=lambda x: x[1], reverse=True)
    mapping = {}
    new_label = 1
    for old_label, count in sorted_labels:
        mapping[old_label] = new_label
        new_label += 1
    out = {chrom: mapping[label] for chrom, label in cluster_dict.items()}
    return out

# -----------------------------
# Expected Normalization based on trans contact frequencies
# -----------------------------
def normalize_interaction_matrix_expected(raw_matrix):
    """ Normalize the raw interaction matrix using the expected-trans interaction model.
    For each cell (i,j) (i != j):
    - Compute C[i] as the sum of raw interactions for row i (marginal for chromosome i).
    - Let T be the grand total sum of all off-diagonal interactions.
    Then, the expected number of interactions between chromosomes i and j is:
    E_{ij} = 1/2 * ((C_i * C_j)/(T - C_i) + (C_i * C_j)/(T - C_j))
    The normalized value is defined as:
    norm_{ij} = observed_{ij} / E_{ij}
    Self-interactions (i == j) remain as NaN.
    """
    # Work on copies to avoid mutating the input
    raw_clean = raw_matrix.copy()
    norm_matrix = raw_matrix.copy()
    
    # First, ensure the raw matrix contains only scalars
    for i in raw_clean.index:
        for j in raw_clean.columns:
            val = raw_clean.at[i, j]
            if isinstance(val, pd.Series):
                raw_clean.at[i, j] = val.iloc[0] if len(val) > 0 else np.nan
    
    # Calculate marginal totals (using row sums)
    marginals = {}
    for chrom in raw_clean.index:
        row_vals = raw_clean.loc[chrom].values
        # Filter out NaN, inf, etc.
        valid_vals = [v for v in row_vals if np.isscalar(v) and np.isfinite(v)]
        marginals[chrom] = sum(valid_vals)
    
    # Calculate total sum of all off-diagonal interactions
    T = sum(marginals.values())
    
    for i in raw_clean.index:
        for j in raw_clean.columns:
            if i == j:
                continue  # skip self-interactions
            
            raw_val = raw_clean.at[i, j]
            # Make sure we have a scalar value
            if isinstance(raw_val, pd.Series):
                raw_val = raw_val.iloc[0] if len(raw_val) > 0 else np.nan
            
            if not np.isnan(raw_val) and np.isfinite(raw_val):
                C_i = marginals[i]
                C_j = marginals[j]
                # Avoid division by zero
                if abs(T - C_i) < 1e-10 or abs(T - C_j) < 1e-10:
                    expected = np.nan
                else:
                    expected = 0.5 * ((C_i * C_j)/(T - C_i) + (C_i * C_j)/(T - C_j))
                # If expected is very small, flag it to avoid division errors
                if abs(expected) < 1e-10:
                    norm_matrix.at[i, j] = np.nan
                else:
                    norm_matrix.at[i, j] = raw_val / expected
            else:
                norm_matrix.at[i, j] = np.nan
                
    return norm_matrix


# -----------------------------
# read the pairs files
# -----------------------------
def parse_pairs_files(pairs_files, target_chroms=None):
    counts = {}
    for pf in pairs_files:
        with open(pf, "r", encoding="utf-8", errors="ignore") as f:
            for line in f:
                line=line.strip()
                if not line or line.startswith("#"):
                    continue
                parts = line.split()
                if len(parts)<6:
                    continue
                query = parts[1]
                target = parts[4]
                if query==target:
                    continue
                if target_chroms is not None:
                    if (query not in target_chroms) and (target not in target_chroms):
                        continue
                counts.setdefault(query, {})
                counts[query][target] = counts[query].get(target,0)+1
    return counts

def read_order_file(order_file):
    out = []
    with open(order_file, 'r') as f:
        for line in f:
            line=line.strip()
            if line:
                out.append(line)
    return out

def build_interaction_matrix(counts, chrom_order):
    matrix = pd.DataFrame(0, index=chrom_order, columns=chrom_order, dtype=float)
    for q in counts:
        for t, cval in counts[q].items():
            if q in matrix.index and t in matrix.columns:
                matrix.at[q,t] = cval
    np.fill_diagonal(matrix.values, np.nan)
    return matrix

def symmetrize_matrix(raw_matrix):
    sym = (raw_matrix + raw_matrix.T)/2
    np.fill_diagonal(sym.values, np.nan)
    return sym

def normalize_symmetric_by_row_max(mat):
    mat = mat.copy()
    np.fill_diagonal(mat.values, np.nan)
    row_max = mat.max(axis=1, skipna=True)
    out = mat.divide(row_max, axis=0)
    return out

# Build sorted table
def build_sorted_interaction_table(matrix):
    rows=[]
    for q in matrix.index:
        interactions=[]
        for t in matrix.columns:
            if t==q:
                continue
            val = matrix.at[q,t]
            if not np.isscalar(val):
                try:
                    val = val.item()
                except (ValueError, AttributeError):
                    try:
                        val = float(val)
                    except (ValueError, TypeError):
                        val = np.nan
            sort_val = safe_float(val) if pd.notnull(val) else -np.inf
            interactions.append((t, sort_val, val))
        interactions.sort(key=lambda x: x[1], reverse=True)
        row = [q]
        for t,_,v in interactions:
            row.append(f"{t} ({v:.2f})")
            row.append(str(v))
        rows.append(row)
    max_len = max(len(r) for r in rows)
    for r in rows:
        while len(r)<max_len:
            r.append("")
    col_names = ["QueryChromosome"]
    for i in range(1, max_len):
        if i%2==1:
            col_names.append(f"Rank{i} (val)")
        else:
            col_names.append(f"Rank{(i)//2}_raw")
    return pd.DataFrame(rows, columns=col_names)

def build_matrix_table(matrix):
    df = matrix.copy()
    df.insert(0,"QueryChromosome", df.index)
    return df

def write_combined_excel(sorted_table, matrix_table, sample_name, out_file):
    import xlsxwriter
    with pd.ExcelWriter(out_file, engine='xlsxwriter') as writer:
        sorted_table.to_excel(writer, sheet_name="SortedInteractions", startrow=1, index=False)
        matrix_table.to_excel(writer, sheet_name="Matrix", startrow=1, index=False)
        ws_s = writer.sheets["SortedInteractions"]
        ws_m = writer.sheets["Matrix"]
        ws_s.write(0,0, f"Sample: {sample_name} - Sorted Interaction Table")
        ws_m.write(0,0, f"Sample: {sample_name} - Interaction Matrix")
    print(f"Combined Excel file saved to {out_file}")

def write_clusters(cluster_dict, out_file):
    df = pd.DataFrame(list(cluster_dict.items()), columns=["QueryChromosome","Cluster"])
    df.to_csv(out_file, sep="\t", index=False)
    print(f"Cluster assignments written to {out_file}")

def cluster_chromosomes(matrix, nclusters=None, thresh=None):
    matrix_filled = matrix.copy()
    for r in matrix_filled.index:
        row_arr = matrix_filled.loc[r].astype(float).values
        max_val = np.nanmax(row_arr)
        fill_val = (max_val+1) if not np.isnan(max_val) else 0
        matrix_filled.loc[r] = matrix_filled.loc[r].fillna(fill_val)
    dists = pdist(matrix_filled.values, metric="euclidean")
    Z = linkage(dists, method="complete")
    print("Linkage distances (for each merge):", Z[:, 2])
    if nclusters is not None:
        labs = fcluster(Z, t=nclusters, criterion="maxclust")
    elif thresh is not None:
        labs = fcluster(Z, t=thresh, criterion="distance")
    else:
        def_thresh = 0.7*np.max(Z[:,2])
        labs = fcluster(Z, t=def_thresh, criterion="distance")
    out = { rname: labs[i] for i,rname in enumerate(matrix.index)}
    return out, Z

def plot_heatmap(matrix, sample_name, out_file_base, dpi=600,
                 global_vmin=None, global_vmax=None, label_fontsize=12,
                 hide_row_dendrogram=False):
    """Generate clustered heatmap with hierarchical clustering on both axes.
    Saves both PNG and PDF versions.
    
    Parameters
    ----------
    global_vmin, global_vmax : float, optional
        If provided, use these as the shared colorscale limits across all plots.
    label_fontsize : int
        Font size for chromosome tick labels.
    hide_row_dendrogram : bool
        If True, hide the left-side (row) dendrogram to save horizontal space.
    """
    row_filled = matrix.copy()
    for r in row_filled.index:
        arr = row_filled.loc[r].astype(float).values
        max_val = np.nanmax(arr)
        fill_val = (max_val+1) if not np.isnan(max_val) else 0
        row_filled.loc[r] = row_filled.loc[r].fillna(fill_val)
    row_dists = pdist(row_filled.values, metric="euclidean")
    Z_rows = linkage(row_dists, method="complete")

    col_filled = matrix.copy().T
    for c in col_filled.index:
        arr = col_filled.loc[c].astype(float).values
        max_val = np.nanmax(arr)
        fill_val = (max_val+1) if not np.isnan(max_val) else 0
        col_filled.loc[c] = col_filled.loc[c].fillna(fill_val)
    col_dists = pdist(col_filled.values, metric="euclidean")
    Z_cols = linkage(col_dists, method="complete")

    mat_display = matrix.copy()
    cmap = sns.color_palette("viridis", as_cmap=True)
    cmap.set_bad("black")
    finite_vals = mat_display.values[np.isfinite(mat_display.values)]
    # Use global scale if provided, otherwise per-sample
    if global_vmin is not None and global_vmax is not None:
        min_val = global_vmin
        max_val = global_vmax
    else:
        min_val = np.min(finite_vals) if finite_vals.size > 0 else 0
        max_val = np.max(finite_vals) if finite_vals.size > 0 else 1
    norm_cont = mcolors.Normalize(vmin=min_val, vmax=max_val)

    g = sns.clustermap(
        mat_display,
        row_linkage=Z_rows,
        col_linkage=Z_cols,
        cmap=cmap,
        norm=norm_cont,
        vmin=min_val,
        vmax=max_val,
        figsize=(12,12)
    )
    r_order = g.dendrogram_row.reordered_ind
    c_order = g.dendrogram_col.reordered_ind
    r_labels = mat_display.index[r_order]
    c_labels = mat_display.columns[c_order]
    g.ax_heatmap.set_xticks(np.arange(len(c_labels)) +0.5)
    g.ax_heatmap.set_xticklabels(c_labels, fontsize=label_fontsize, rotation=90, family='Arial')
    g.ax_heatmap.set_yticks(np.arange(len(r_labels)) +0.5)
    g.ax_heatmap.set_yticklabels(r_labels, fontsize=label_fontsize, rotation=0, family='Arial')
    # Dendrogram styling
    if hide_row_dendrogram:
        g.ax_row_dendrogram.set_visible(False)
        # Reclaim the space: shrink the row dendrogram column
        g.gs.update(wspace=0.02)
    else:
        for line in g.ax_row_dendrogram.lines:
            line.set_linewidth(2)
    for line in g.ax_col_dendrogram.lines:
        line.set_linewidth(2)
    cbar = g.ax_heatmap.collections[0].colorbar
    cbar.set_ticks( np.linspace(min_val, max_val, num=5) )
    cbar.set_ticklabels([f"{x:.1f}" for x in np.linspace(min_val, max_val, num=5)], fontsize=10)
    cbar.ax.set_ylabel("Interaction Value", fontsize=12, family='Arial')

    g.fig.suptitle(
        f"Interaction Heatmap for Sample: {sample_name}\nRows=Query, Columns=Target (Self-interactions NaN)",
        y=1.15, fontsize=14, family='Arial'
    )
    
    # Save both PNG and PDF versions
    png_file = out_file_base.replace('.png', '') + '.png'
    pdf_file = out_file_base.replace('.png', '') + '.pdf'
    
    g.savefig(png_file, dpi=dpi, format='png')
    g.savefig(pdf_file, dpi=dpi, format='pdf')
    plt.close(g.fig)
    print(f"Heatmaps saved: {png_file} and {pdf_file}")

